    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        SimpleDateFormat format=new SimpleDateFormat("dd-MM-yyyy");
        String tanggal = String.valueOf(format.format(txtTanggal.getDate()));
        if (txtNmBarang.getText().isEmpty() || cmbJenis.getSelectedItem().equals("Pilih") || txtStok.getText().isEmpty()
            || txtHarga.getText().isEmpty() || txtKadaluwarsa.getDate().equals("")){
            JOptionPane.showMessageDialog(null, "Data Cannot Be Empty", "Monascho Store", JOptionPane.WARNING_MESSAGE);
        }else{
            DB.setKdBarang(txtKdBarang.getText());
            DB.setNmBarang(txtNmBarang.getText());
            DB.setJenis(cmbJenis.getSelectedItem().toString());
            DB.setStok(Integer.parseInt(txtStok.getText()));
            DB.setHarga(Integer.parseInt(txtHarga.getText()));
            DB.setTanggal(format.format(txtTanggal.getDate()));
            DB.setKadaluwarsa(format.format(txtKadaluwarsa.getDate()));
            if (btnTambah.getText().equalsIgnoreCase("Cancel")) {
                if(bc.tambahBarang(DB)==1) {
                    JOptionPane.showMessageDialog(null, "Data Has Been Saved", "Monascho Store", JOptionPane.INFORMATION_MESSAGE);
                    buatTable();
                    showTable();
                    lebarKolom();
                    try {
                        tampilx();
                    } catch (Exception e) {
                        Logger.getLogger(Barang.class.getName()).log(Level.SEVERE, null, e);
                    }
                }else{
                    JOptionPane.showMessageDialog(null, "Data Failed to Save", "Monascho Store", JOptionPane.WARNING_MESSAGE);
                }
            }
            if (btnEdit.getText().equalsIgnoreCase("Cancel")) {
                if (bc.editBarang(DB)==1) {
                    JOptionPane.showMessageDialog(null, "Data Has Been Changed", "Monascho Store", JOptionPane.INFORMATION_MESSAGE);
                    buatTable();
                    showTable();
                }else{
                    JOptionPane.showMessageDialog(null, "Data Failed to Change", "Monascho Store", JOptionPane.WARNING_MESSAGE);
                }
            }
            bersih();
            siapIsi(false);
            btnTambah.setText("Add");
            btnEdit.setText("Edit");
            tombolNormal();
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

