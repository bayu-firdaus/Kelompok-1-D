    private void buatTable() {
        model = new DefaultTableModel();
        model.addColumn("Tanggal_Masuk");
        model.addColumn("Kode Barang");
        model.addColumn("Nama Barang");
        model.addColumn("Jenis");
        model.addColumn("Stok");
        model.addColumn("Harga");
        model.addColumn("Kadaluwarsa");
        tblBarang.setModel(model);
        lebarKolom();
    }
    
    private void showTable() {
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        ListBarang.clear();
        ListBarang = bc.tampil();
        for (x=0; x<ListBarang.size(); x++) {
            Object[] data = new Object[7];
            data[0]=ListBarang.get(x).getTanggal();
            data[1]=ListBarang.get(x).getKdBarang();
            data[2]=ListBarang.get(x).getNmBarang();
            data[3]=ListBarang.get(x).getJenis();
            data[4]=ListBarang.get(x).getStok();
            data[5]=ListBarang.get(x).getHarga();
            data[6]=ListBarang.get(x).getKadaluwarsa();
            model.addRow(data);
        }
        lebarKolom();
    }
    
